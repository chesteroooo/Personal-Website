//carousel1
document.addEventListener('DOMContentLoaded', function() {
    const carouselOverlay1 = document.getElementById('carousel-overlay1');
    const closeModalBtn1 = document.getElementById('btn-close1');

    document.querySelector('.carousel1').addEventListener('click', function(event) {
        event.preventDefault(); // 阻止默认链接行为
        carouselOverlay1.classList.add('show');
    });

    closeModalBtn1.addEventListener('click', function() {
        carouselOverlay1.classList.remove('show');
    });
});

